void turno()
{
    for(int i=0; i<15; i++){
        for(int j=0; j<15; j++){
            switch(m[mundoActual][i][j]){
                case 'M':
                    if(m[mundoActual][i][j+1]=='.')
                    {
                        m[mundoActual][i][j]='.';
                        j++;
                        m[mundoActual][i][j]='M';
                    }
                    else
                    {
                         m[mundoActual][i][j]='W';
                    }
                break;
                case 'W':
                    if(m[mundoActual][i][j-1]=='.')
                    {
                        m[mundoActual][i][j]='.';
                        m[mundoActual][i][j-1]='W';
                    }
                    else
                    {
                         m[mundoActual][i][j]='M';
                    }
                break;
                case 'N':
                    if(m[mundoActual][i+1][j]=='.')
                    {
                        m[mundoActual][i][j]='.';
                        m[mundoActual][i+1][j]='N';
                    }
                    else
                    {
                         m[mundoActual][i][j]='H';
                    }
                break;
                case 'H':
                    if(m[mundoActual][i-1][j]=='.')
                    {
                        m[mundoActual][i][j]='.';
                        m[mundoActual][i-1][j]='H';
                    }
                    else
                    {
                         m[mundoActual][i][j]='N';
                    }
                break;
                case 'v':
                    if(uno%2==0)
                        if(m[mundoActual][i+1][j]=='.')
                            m[mundoActual][i+1][j]='i';
                break;
                case '>':
                    if(uno%2==0)
                       if(m[mundoActual][i][j+1]=='.')
                       {
                            j++;
                            m[mundoActual][i][j+1]='o';
                       }


                break;
                case '<':
                    if(uno%2==0)
                        if(m[mundoActual][i][j-1]=='.')
                            m[mundoActual][i][j-1]='*';
                break;
                case '^':
                    if(uno%2==0)
                        if(m[mundoActual][i-1][j]=='.')
                            m[mundoActual][i-1][j]='l';
                break;
                case 'i':
                    if(m[mundoActual][i+1][j]!='#')
                    {
                        m[mundoActual][i][j]='.';
                        m[mundoActual][i+1][j]='i';
                    }
                    else
                        m[mundoActual][i][j]='.';
                break;
                case 'o':
                    if(m[mundoActual][i][j+1]!='#')
                    {
                        m[mundoActual][i][j]='.';
                        j++;
                        m[mundoActual][i][j+1]='o';
                    }
                    else
                        m[mundoActual][i][j]='.';
                break;
                case '*':
                    if(m[mundoActual][i][j-1]!='#')
                    {
                        m[mundoActual][i][j]='.';
                        m[mundoActual][i][j-1]='*';
                    }
                    else
                        m[mundoActual][i][j]='.';
                break;
                case 'l':
                    if(m[mundoActual][i-1][j]!='#')
                    {
                        m[mundoActual][i][j]='.';
                        m[mundoActual][i-1][j]='l';
                    }
                    else
                        m[mundoActual][i][j]='.';
                break;
                default:
                break;

            }
            uno++;
        }
    }
}

